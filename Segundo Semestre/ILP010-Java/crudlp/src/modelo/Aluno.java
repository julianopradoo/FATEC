package modelo;

/**
 *
 * @author Juliano
 */
public class Aluno {
    String nome;
    String cpf;
    String nascimento;
    String altura;
    String peso;
    String imc;

    public Aluno(String nome, String cpf, String nascimento, String altura, String peso, String imc) {
        this.nome = nome;
        this.cpf = cpf;
        this.nascimento = nascimento;
        this.altura = altura;
        this.peso = peso;
        this.imc = imc;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getNascimento() {
        return nascimento;
    }

    public void setNascimento(String nascimento) {
        this.nascimento = nascimento;
    }

    public String getAltura() {
        return altura;
    }

    public void setAltura(String altura) {
        this.altura = altura;
    }

    public String getPeso() {
        return peso;
    }

    public void setPeso(String peso) {
        this.peso = peso;
    }

    public String getImc() {
        return imc;
    }

    public void setImc(String imc) {
        this.imc = imc;
    }
    
    
    
}
 
 