    private void btnatualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnatualizarActionPerformed
        DefaultTableModel tbl = (DefaultTableModel)tblregistro.getModel();
      
            int SelectedRowIndex = tblregistro.getSelectedRow();
            
            String nnome = tbl.getValueAt(SelectedRowIndex, 0).toString();
            String ncpf = tbl.getValueAt(SelectedRowIndex, 1).toString();
            String nnascimento = tbl.getValueAt(SelectedRowIndex, 2).toString();
            String naltura = tbl.getValueAt(SelectedRowIndex, 3).toString();
            String npeso = tbl.getValueAt(SelectedRowIndex, 4).toString();
            String nimc = tbl.getValueAt(SelectedRowIndex, 5).toString();
            
            String novonome = JOptionPane.showInputDialog(null, "Digite o novo nome", nnome);
            String novocpf = JOptionPane.showInputDialog(null, "Digite o novo cpf", ncpf);
            String novonascimento = JOptionPane.showInputDialog(null, "Digite o novo nascimento", nnascimento);
            String novoaltura = JOptionPane.showInputDialog(null, "Digite o novo altura", naltura);
            String novopeso = JOptionPane.showInputDialog(null, "Digite o novo peso", npeso);
            double daltura = Double.parseDouble(novoaltura);
            double dpeso = Double.parseDouble(novopeso);
            double dimc = dpeso/ (daltura * daltura);
            String novoimc = Double.toString(dimc);
            
            AlunoDAO alunodao = new AlunoDAO();
        
        
             Aluno aluno = new Aluno(novonome, novocpf, novonascimento, novoaltura, novopeso, novoimc);
             alunodao.atualiza(aluno, ncpf);
        
             alunodao.seleciona(tblregistro);
            
        
    }//GEN-LAST:event_btnatualizarActionPerformed

