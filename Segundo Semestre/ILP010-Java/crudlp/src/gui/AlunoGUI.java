    private void btninserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninserirActionPerformed
        String nome = txtnome.getText();
        String cpf = txtcpf.getText();
        String nascimento = txtnascimento.getText();
        String altura = txtaltura.getText();
        String peso = txtpeso.getText();
        
        double a1 = Double.valueOf(altura).doubleValue();
        double p1  = Double.valueOf(peso).doubleValue();
        double imc1 = ( p1 / (a1 * a1));
        
        String strResultado = Double.toString(imc1);
        txtimc.setText(strResultado);
        String imc = txtimc.getText();
        
    
        AlunoDAO alunodao = new AlunoDAO();
        
        
        Aluno aluno = new Aluno(nome, cpf, nascimento, altura, peso, imc);
        alunodao.adiciona(aluno);
        
        alunodao.seleciona(tblregistro);
        
      
    }//GEN-LAST:event_btninserirActionPerformed

