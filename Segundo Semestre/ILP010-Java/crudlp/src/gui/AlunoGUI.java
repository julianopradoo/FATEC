    private void btncadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncadastrarActionPerformed
        // TODO add your handling code here:
        int row = tblregistro.getSelectedRow();
        String nome = tblregistro.getModel().getValueAt(row,0).toString();
        String cpf = tblregistro.getModel().getValueAt(row,1).toString();
        double imc = Double.parseDouble(tblregistro.getModel().getValueAt(row,5).toString());
        String classificacao = "";
        
        if (imc < 18.5) {
             classificacao = "Baixo peso";
        } else if (imc < 25) {
             classificacao = "Peso normal";
        } else if (imc < 30) {
             classificacao = "Sobrepeso";
        } else if (imc < 35) {
             classificacao = "Obesidade grau 1";
        } else if (imc < 40) {
             classificacao = "Obesidade grau 2";
        } else {
             classificacao = "Obesidade grau 3";
        }
        
        
        
        
        try {
            DateTimeFormatter formato =  DateTimeFormatter.ofPattern("dd/MM/yyyy");
            File file = new File("File.txt");
            FileWriter filewriter = new FileWriter(file, true);
            BufferedWriter bufferedwriter = new BufferedWriter(filewriter);
            bufferedwriter.write(LocalDateTime.now().format(formato));
            bufferedwriter.write(" " + nome);
            bufferedwriter.write(" " + cpf);
            bufferedwriter.write(" " + imc);
            bufferedwriter.write(" " + classificacao);
            bufferedwriter.newLine();
            bufferedwriter.close();
        } catch (IOException ex){
            
        }
       
    }//GEN-LAST:event_btncadastrarActionPerformed

 
    
    
